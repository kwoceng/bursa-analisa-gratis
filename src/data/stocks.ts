export function getStock(kode: string): Stock | undefined {
  return stocks.find((s) => s.kode.toUpperCase() === kode.toUpperCase());
}

// Deterministic pseudo-random walk untuk grafik demo 30 hari.
export function generateHistory(kode: string, days = 30): { tanggal: string; harga: number }[] {
  const stock = getStock(kode);
  if (!stock) return [];
  let seed = 0;
  for (let i = 0; i < kode.length; i++) seed = (seed * 31 + kode.charCodeAt(i)) >>> 0;
  const rand = () => {
    seed = (seed * 1664525 + 1013904223) >>> 0;
    return (seed & 0xffff) / 0xffff;
  };
  const today = new Date();
  const points: { tanggal: string; harga: number }[] = [];
  // Mulai dari harga sekitar low52+high52 rata-rata, akhiri di harga sekarang.
  const endPrice = stock.harga;
  const startPrice = Math.round((stock.low52 + stock.high52) / 2);
  for (let i = days - 1; i >= 0; i--) {
    const d = new Date(today);
    d.setDate(today.getDate() - i);
    const t = (days - 1 - i) / (days - 1);
    const trend = startPrice + (endPrice - startPrice) * t;
    const noise = (rand() - 0.5) * stock.harga * 0.04;
    const harga = Math.max(1, Math.round(trend + noise));
    points.push({
      tanggal: d.toLocaleDateString("id-ID", { day: "2-digit", month: "short" }),
      harga: i === 0 ? endPrice : harga,
    });
  }
  return points;
}

